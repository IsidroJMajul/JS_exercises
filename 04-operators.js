// Operadores

// Aritméticos
// + | - | * | / | % módulo | ** exponencial
// ++ incremento | -- decremento

let a = 5
a++
a++

console.log (a)

// Asignación
// = | += asigna y suma unidades a las ya asignadas | se puede hacer con los demás operadores aritméticos

let myVariable = 2
myVariable += 4
console.log (myVariable)

// Comparación
//  > | < | <= | >= | == igualdad por VALOR
// === igualdad por VALOR y TIPO DE DATO
// != | !== | !===

console.log (a == 7)
console.log (a == "7")

console.log (a === 7)
console.log (a === "7")

// Lógicos
// && and | || or | ! not

// Ternarios
// Es otra forma de escribir condicionales
// ... ? ... :

const isRaining = false
// si se cumple, se ejecuta lo siguiente al signo ?
// si no se cumple la condición, se va a ejecutar lo seguido a los :
isRaining ? console.log("está lloviendo"): console.log("no está lloviendo")


